export const useGetAmountByDay = (date: Date) => {
  const income: number | undefined = mockIncomeMap.get(
    date.toISOString().slice(0, 10)
  );
  const outcome: number | undefined = mockOutcomeMap.get(
    date.toISOString().slice(0, 10)
  );

  return {
    income: income,
    outcome: outcome,
  };
};

